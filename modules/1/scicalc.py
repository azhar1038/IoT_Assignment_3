from math import sqrt, sin, cos, tan, log, exp

def addition(a, b):
    return a+b

def subtraction(a, b):
    return a-b

def division(a, b):
    return a/b

def multiplication(a, b):
    return a*b

def modulus(a, b):
    return a%b

def power(a, b):
    return a**b

def square_root(n):
    return sqrt(n)

def cubic_root(n):
    return n**(1/3)

def sinx(x):
    return sin(x)

def cosx(x):
    return cos(x)

def tanx(x):
    return tan(x)

def logx(x):
    return log(x)

def expx(x):
    return exp(x)

def absolute(x):
    return abs(x)

